package com.bimils.myapp.board.model;

public class BoardEffect {
	
	private int bo_seq_no;
	private String effect_class;
	private String effect_start;
	private String effect_end;
	private String effect_del;
	
	
	public int getBo_seq_no() {
		return bo_seq_no;
	}
	public void setBo_seq_no(int bo_seq_no) {
		this.bo_seq_no = bo_seq_no;
	}
	public String getEffect_class() {
		return effect_class;
	}
	public void setEffect_class(String effect_class) {
		this.effect_class = effect_class;
	}
	public String getEffect_start() {
		return effect_start;
	}
	public void setEffect_start(String effect_start) {
		this.effect_start = effect_start;
	}
	public String getEffect_end() {
		return effect_end;
	}
	public void setEffect_end(String effect_end) {
		this.effect_end = effect_end;
	}
	public String getEffect_del() {
		return effect_del;
	}
	public void setEffect_del(String effect_del) {
		this.effect_del = effect_del;
	}
	
	
}
