package com.bimils.myapp.common.sms.model;

public class SmsMsg {
	
	private String sms_seq_no;
	private String sms_phone_no;
	private String sms_title;
	private String sms_content;
	private String sms_status;
	private String sms_reg_date;
	private String sms_snd_date;
	
	public String getSms_seq_no() {
		return sms_seq_no;
	}
	public void setSms_seq_no(String sms_seq_no) {
		this.sms_seq_no = sms_seq_no;
	}
	public String getSms_phone_no() {
		return sms_phone_no;
	}
	public void setSms_phone_no(String sms_phone_no) {
		this.sms_phone_no = sms_phone_no;
	}
	public String getSms_title() {
		return sms_title;
	}
	public void setSms_title(String sms_title) {
		this.sms_title = sms_title;
	}
	public String getSms_content() {
		return sms_content;
	}
	public void setSms_content(String sms_content) {
		this.sms_content = sms_content;
	}
	public String getSms_status() {
		return sms_status;
	}
	public void setSms_status(String sms_status) {
		this.sms_status = sms_status;
	}
	public String getSms_reg_date() {
		return sms_reg_date;
	}
	public void setSms_reg_date(String sms_reg_date) {
		this.sms_reg_date = sms_reg_date;
	}
	public String getSms_snd_date() {
		return sms_snd_date;
	}
	public void setSms_snd_date(String sms_snd_date) {
		this.sms_snd_date = sms_snd_date;
	}

}
